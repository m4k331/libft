#include "libft.h"

void		*ft_vback_con(t_vector *v)
{
	if (v->iter > 0)
	{
		v->iter--;
		return (ft_vcurr_con(v));
	}
	if (v->iter == 0)
		v->iter--;
	return (NULL);
}
