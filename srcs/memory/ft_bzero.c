#include "libft.h"

void			ft_bzero(void *s, size_t n)
{
	uint64_t	*big;
	uint8_t		*small;

	big = (uint64_t*)s;
	while (n > 7ULL)
	{
		*big++ = 0ULL;
		n -= 8ULL;
	}
	small = (uint8_t*)big;
	while (n--)
		*small++ = 0;
}
